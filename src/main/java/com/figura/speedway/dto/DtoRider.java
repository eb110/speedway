package com.figura.speedway.dto;

public class DtoRider {

    public DtoRider() {
    }

    private int id;
    private int nr;
    private String pkt;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNr() {
        return nr;
    }

    public void setNr(int nr) {
        this.nr = nr;
    }

    public String getPkt() {
        return pkt;
    }

    public void setPkt(String pkt) {
        this.pkt = pkt;
    }
}
